static bool ts_lex(TSLexer *lexer, TSStateId state) {
  START_LEXER();
  eof = lexer->eof(lexer);
  switch (state) {
    case 0:
      if (eof) ADVANCE(9);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(15);
      if (lookahead == '\n') ADVANCE(14);
      if (lookahead == 11 ||
          lookahead == '\f') ADVANCE(11);
      if (lookahead == '\r') ADVANCE(1);
      if (lookahead != 0) ADVANCE(12);
      END_STATE();
    case 1:
      if (lookahead == '\n') ADVANCE(14);
      if (lookahead == ':') ADVANCE(7);
      if (lookahead == '\t' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(3);
      END_STATE();
    case 2:
      if (lookahead == '\n') ADVANCE(13);
      END_STATE();
    case 3:
      if (lookahead == ':') ADVANCE(7);
      if (lookahead == '\t' ||
          lookahead == '\n' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(3);
      END_STATE();
    case 4:
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(15);
      if (lookahead == '\n') ADVANCE(14);
      if (lookahead == 11 ||
          lookahead == '\f') ADVANCE(17);
      if (lookahead == '\r') ADVANCE(1);
      END_STATE();
    case 5:
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(17);
      if (lookahead == '\n') ADVANCE(13);
      if (lookahead == 11 ||
          lookahead == '\f') ADVANCE(11);
      if (lookahead == '\r') ADVANCE(2);
      if (lookahead != 0) ADVANCE(12);
      END_STATE();
    case 6:
      if (lookahead == '\t' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(6);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(12);
      END_STATE();
    case 7:
      if (lookahead == '\t' ||
          lookahead == '\n' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(10);
      END_STATE();
    case 8:
      if (eof) ADVANCE(9);
      if (lookahead == '\t' ||
          lookahead == 11 ||
          lookahead == '\f' ||
          lookahead == ' ') ADVANCE(17);
      if (lookahead == '\n') ADVANCE(13);
      if (lookahead == '\r') ADVANCE(2);
      END_STATE();
    case 9:
      ACCEPT_TOKEN(ts_builtin_sym_end);
      END_STATE();
    case 10:
      ACCEPT_TOKEN(aux_sym_classifiers_token1);
      if (lookahead == '\t' ||
          lookahead == '\n' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(10);
      END_STATE();
    case 11:
      ACCEPT_TOKEN(aux_sym_target_token1);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(16);
      if (lookahead == 11 ||
          lookahead == '\f') ADVANCE(11);
      if (lookahead == '\r') ADVANCE(6);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(12);
      END_STATE();
    case 12:
      ACCEPT_TOKEN(aux_sym_target_token1);
      if (lookahead == '\t' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(6);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(12);
      END_STATE();
    case 13:
      ACCEPT_TOKEN(sym___newline);
      END_STATE();
    case 14:
      ACCEPT_TOKEN(sym___newline);
      if (lookahead == ':') ADVANCE(7);
      if (lookahead == '\t' ||
          lookahead == '\n' ||
          lookahead == '\r' ||
          lookahead == ' ') ADVANCE(3);
      END_STATE();
    case 15:
      ACCEPT_TOKEN(sym___whitespace);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(15);
      if (lookahead == 11 ||
          lookahead == '\f') ADVANCE(17);
      if (lookahead == ':') ADVANCE(7);
      if (('\n' <= lookahead && lookahead <= '\r')) ADVANCE(3);
      END_STATE();
    case 16:
      ACCEPT_TOKEN(sym___whitespace);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(16);
      if (lookahead == 11 ||
          lookahead == '\f') ADVANCE(11);
      if (lookahead == '\r') ADVANCE(6);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(12);
      END_STATE();
    case 17:
      ACCEPT_TOKEN(sym___whitespace);
      if (lookahead == '\t' ||
          lookahead == 11 ||
          lookahead == '\f' ||
          lookahead == ' ') ADVANCE(17);
      END_STATE();
    default:
      return false;
  }
}

