      END_STATE();
    case 15:
      if (lookahead == ']') ADVANCE(29);
      if (lookahead == '-' ||
          lookahead == '.' ||
          lookahead == '_') ADVANCE(28);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(15);
      if (('A' <= lookahead && lookahead <= 'Z') ||
          ('a' <= lookahead && lookahead <= 'z')) ADVANCE(18);
      END_STATE();
    case 16:
      if (lookahead == ']') ADVANCE(29);
