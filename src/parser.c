      if (lookahead == '`') ADVANCE(6);
      if (lookahead != 0) ADVANCE(8);
      END_STATE();
    case 12:
      if (lookahead == ':') ADVANCE(10);
      if (lookahead == '+' ||
          lookahead == '-' ||
          lookahead == '.' ||
          lookahead == '_') ADVANCE(34);
      if (('0' <= lookahead && lookahead <= '9') ||
          ('A' <= lookahead && lookahead <= 'Z') ||
          ('a' <= lookahead && lookahead <= 'z')) ADVANCE(12);
      END_STATE();
    case 13:
      if (lookahead == '[') ADVANCE(35);
      END_STATE();
    case 14:
