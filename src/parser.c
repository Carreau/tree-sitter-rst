static bool ts_lex(TSLexer *lexer, TSStateId state) {
  START_LEXER();
  eof = lexer->eof(lexer);
  switch (state) {
    case 0:
      if (eof) ADVANCE(2);
      if (lookahead == '\t') ADVANCE(4);
      if (lookahead == 11) ADVANCE(5);
      if (lookahead == '\f') ADVANCE(6);
      if (lookahead == ' ') ADVANCE(3);
      if (lookahead == '|') ADVANCE(8);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(7);
      END_STATE();
    case 1:
      if (lookahead == '\t') ADVANCE(4);
      if (lookahead == 11) ADVANCE(5);
      if (lookahead == '\f') ADVANCE(6);
      if (lookahead == ' ') ADVANCE(3);
      END_STATE();
    case 2:
      ACCEPT_TOKEN(ts_builtin_sym_end);
      END_STATE();
    case 3:
      ACCEPT_TOKEN(anon_sym_);
      END_STATE();
    case 4:
      ACCEPT_TOKEN(anon_sym_TAB);
      END_STATE();
    case 5:
      ACCEPT_TOKEN(anon_sym_2);
      END_STATE();
    case 6:
      ACCEPT_TOKEN(anon_sym_3);
      END_STATE();
    case 7:
      ACCEPT_TOKEN(aux_sym_field_token2);
      END_STATE();
    case 8:
      ACCEPT_TOKEN(anon_sym_PIPE);
      END_STATE();
    case 9:
      ACCEPT_TOKEN(aux_sym__line_token1);
      if (lookahead == '\t' ||
          lookahead == 11 ||
          lookahead == '\f' ||
          lookahead == ' ') ADVANCE(9);
      END_STATE();
    case 10:
      ACCEPT_TOKEN(aux_sym__line_token1);
      if (eof) ADVANCE(2);
      if (lookahead == '\t' ||
          lookahead == 11 ||
          lookahead == '\f' ||
          lookahead == ' ') ADVANCE(9);
      END_STATE();
    default:
      return false;
  }
}

